class A
{
    public function foo()
    {
        static $x = 0;
        echo ++$x;
    }
}
class B extends A
{
}
$a1 = new A(); // создаём экземпляр класса А
$b1 = new B(); // создаём экземпляр класса В
$a1->foo(); // 1 $x=1 в классе А
$b1->foo(); // 1 х=1 в классе В
$a1->foo(); // 2 х=2 в классе А
$b1->foo(); // 2 х=2 в классе В
